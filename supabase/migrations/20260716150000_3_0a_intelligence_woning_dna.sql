create table if not exists public.intelligence_trends (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  woning_id bigint not null
    references public.woningen(id)
    on update cascade
    on delete cascade,

  trend_type text not null
    check (
      trend_type in (
        'inspectiekwaliteit',
        'orde_netheid',
        'schade',
        'meldingen',
        'taken',
        'controletijd',
        'energie'
      )
    ),

  periode_van date not null,
  periode_tot date not null,

  meetwaarde numeric null,
  referentiewaarde numeric null,
  afwijking_percentage numeric null,

  richting text not null default 'stabiel'
    check (
      richting in (
        'dalend',
        'stabiel',
        'stijgend',
        'onvoldoende_data'
      )
    ),

  ernst text not null default 'informatie'
    check (
      ernst in (
        'informatie',
        'aandacht',
        'hoog',
        'kritiek'
      )
    ),

  bron_aantal integer not null default 0
    check (bron_aantal >= 0),

  toelichting text null,
  details jsonb not null default '{}'::jsonb,

  berekend_at timestamptz not null default now(),

  constraint intelligence_trends_periode_geldig
    check (periode_tot >= periode_van),

  constraint intelligence_trends_uniek
    unique (
      woning_id,
      trend_type,
      periode_van,
      periode_tot
    )
);

create index if not exists
  intelligence_trends_woning_idx
on public.intelligence_trends(
  woning_id,
  periode_tot desc
);

create index if not exists
  intelligence_trends_ernst_idx
on public.intelligence_trends(
  ernst,
  periode_tot desc
);

create table if not exists public.woning_dna_snapshots (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),

  woning_id bigint not null
    references public.woningen(id)
    on update cascade
    on delete cascade,

  peildatum date not null default current_date,

  inspecties_aantal integer not null default 0,
  gemiddelde_orde_netheid numeric null,
  inspecties_met_schade integer not null default 0,

  meldingen_open integer not null default 0,
  meldingen_hoog_spoed integer not null default 0,

  taken_open integer not null default 0,
  taken_over_deadline integer not null default 0,

  gemiddelde_controletijd_minuten numeric null,

  risicoscore integer not null default 0
    check (risicoscore between 0 and 100),

  risiconiveau text not null default 'laag'
    check (
      risiconiveau in (
        'laag',
        'middel',
        'hoog',
        'kritiek'
      )
    ),

  sterke_punten jsonb not null default '[]'::jsonb,
  aandachtspunten jsonb not null default '[]'::jsonb,
  kenmerken jsonb not null default '{}'::jsonb,

  berekend_at timestamptz not null default now(),

  constraint woning_dna_snapshot_uniek
    unique (woning_id, peildatum)
);

create index if not exists
  woning_dna_snapshots_woning_idx
on public.woning_dna_snapshots(
  woning_id,
  peildatum desc
);

create index if not exists
  woning_dna_snapshots_risico_idx
on public.woning_dna_snapshots(
  risiconiveau,
  risicoscore desc
);

create or replace function public.intelligence_set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  new.berekend_at = now();
  return new;
end;
$$;

