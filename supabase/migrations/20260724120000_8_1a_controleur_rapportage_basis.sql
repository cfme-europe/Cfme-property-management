create or replace function public.controleer_controle_afwijking_koppeling()
returns trigger
language plpgsql
as $$
begin
  if not exists (
    select 1
    from public.controle_resultaten resultaat
    where resultaat.id = new.controle_resultaat_id
      and resultaat.woning_id = new.woning_id
      and resultaat.controlesessie_id = new.controlesessie_id
      and resultaat.inspectie_id is not distinct from new.inspectie_id
  ) then
    raise exception
      'De afwijking hoort niet bij dit controleresultaat, deze woning en controlesessie.';
  end if;

  if new.melding_id is not null and not exists (
    select 1
    from public.meldingen melding
    where melding.id = new.melding_id
      and melding.woning_id = new.woning_id
  ) then
    raise exception
      'De gekoppelde melding hoort niet bij deze woning.';
  end if;

  if new.taak_id is not null and not exists (
    select 1
    from public.taken taak
    where taak.id = new.taak_id
      and taak.woning_id = new.woning_id
  ) then
    raise exception
      'De gekoppelde taak hoort niet bij deze woning.';
  end if;

  return new;
end;
$$;

drop trigger if exists
  controle_afwijkingen_controle_koppeling
on public.controle_afwijkingen;

create trigger
  controle_afwijkingen_controle_koppeling
before insert or update of
  controle_resultaat_id,
  woning_id,
  inspectie_id,
  controlesessie_id,
  melding_id,
  taak_id
on public.controle_afwijkingen
for each row
execute function public.controleer_controle_afwijking_koppeling();


