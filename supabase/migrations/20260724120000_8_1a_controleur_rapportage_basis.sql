create or replace function public.controleer_woningroute_koppeling()
returns trigger
language plpgsql
as $$
begin
  if new.verdieping_id is not null and not exists (
    select 1
    from public.woning_verdiepingen verdieping
    where verdieping.id = new.verdieping_id
      and verdieping.woning_id = new.woning_id
  ) then
    raise exception
      'De verdieping hoort niet bij deze woning.';
  end if;

  if new.kamer_id is not null and not exists (
    select 1
    from public.kamers kamer
    where kamer.id = new.kamer_id
      and kamer.woning_id = new.woning_id
  ) then
    raise exception
      'De kamer hoort niet bij deze woning.';
  end if;

  return new;
end;
$$;

drop trigger if exists
  woning_ruimten_controle_koppeling
