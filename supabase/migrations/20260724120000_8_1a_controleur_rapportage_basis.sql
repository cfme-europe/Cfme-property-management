create table if not exists public.woning_verdiepingen (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  woning_id bigint not null
    references public.woningen(id)
    on update cascade
    on delete restrict,

  naam text not null,
  niveau integer not null default 0,
  loopvolgorde integer not null default 0
    check (loopvolgorde >= 0),

  actief boolean not null default true,
  opmerkingen text,

  constraint woning_verdiepingen_naam_niet_leeg
    check (length(trim(naam)) > 0),

  constraint woning_verdiepingen_naam_uniek
    unique (woning_id, naam)
);

create index if not exists
  woning_verdiepingen_woning_idx
on public.woning_verdiepingen(woning_id);

create index if not exists
  woning_verdiepingen_loopvolgorde_idx
on public.woning_verdiepingen(
  woning_id,
  actief,
  loopvolgorde,
  id
