create or replace function public.controleer_controle_resultaat_koppeling()
returns trigger
language plpgsql
as $$
declare
  sessie_woning_id bigint;
  inspectie_woning_id bigint;
begin
  select woning_id
  into sessie_woning_id
  from public.controlesessies
  where id = new.controlesessie_id;

  if sessie_woning_id is null
     or sessie_woning_id <> new.woning_id then
    raise exception
      'De controlesessie hoort niet bij deze woning.';
  end if;

  if new.inspectie_id is not null then
    select woning_id
    into inspectie_woning_id
    from public.inspecties
    where id = new.inspectie_id;

    if inspectie_woning_id is null
       or inspectie_woning_id <> new.woning_id then
      raise exception
        'De inspectie hoort niet bij deze woning.';
    end if;
  end if;

  if not exists (
    select 1
    from public.woning_controlepunten controlepunt
    where controlepunt.id = new.woning_controlepunt_id
      and controlepunt.woning_id = new.woning_id
      and controlepunt.ruimte_id = new.ruimte_id
      and controlepunt.object_id
          is not distinct from new.object_id
  ) then
    raise exception
      'Het controlepunt hoort niet bij deze woning, ruimte en object.';
  end if;

  return new;
end;
$$;

