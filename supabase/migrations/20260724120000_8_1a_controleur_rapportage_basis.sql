-- Vervang de oorspronkelijke controle zodat dag- en nachtmeters
-- ook zelfstandig als geldige meteropname kunnen worden geregistreerd.
do $$
declare
  constraint_record record;
begin
  for constraint_record in
    select constraint_naam.conname
    from pg_constraint constraint_naam
    where constraint_naam.conrelid = 'public.meterstanden'::regclass
      and constraint_naam.contype = 'c'
      and pg_get_constraintdef(constraint_naam.oid) ilike '%elektriciteit_kwh%'
      and pg_get_constraintdef(constraint_naam.oid) ilike '%gas_m3%'
      and pg_get_constraintdef(constraint_naam.oid) ilike '%water_m3%'
  loop
    execute format(
      'alter table public.meterstanden drop constraint %I',
      constraint_record.conname
    );
  end loop;
end;
$$;

